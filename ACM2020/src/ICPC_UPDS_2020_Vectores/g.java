package ICPC_UPDS_2020_Vectores;

import java.util.Scanner;

public class g {

	public static void main(String[] args) {
		// Alegría Oriental
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		int c=0;
		while(n-->0) {
			c = sc.nextInt();
			for (int i = 0; i < c; i++) {
				System.out.print('w');
			}
			System.out.println("");
		}
	}

}
